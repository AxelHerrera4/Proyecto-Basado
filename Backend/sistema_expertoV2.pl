top_10_juegos_mejores(ListaTop10) :-
    % Encontrar todos los juegos con sus críticas positivas
    findall([CriticasPos, Juego],
            juego(Juego, _, _, _, _, _, _, CriticasPos, _, _),
            ListaCriticas),
    % Ordenar la lista de juegos en base a las críticas positivas de forma descendente
